package warcraft.util;

public class Custo {

    private double comida;
    private double ouro;
    private double madeira;
    private double mana;

    public Custo(int comida, int ouro, int madeira, int mana) {
        this.comida = comida;
        this.ouro = ouro;
        this.madeira = madeira;
        this.mana = mana;
    }
}
